# P2a reference at matched sens (uses its own softmax outputs, no recalibration)
# ---------------------------------------------------------------------------
def p2a_reference(seed: int, cohort: str, labels_is_pos: dict[str, bool]) -> dict:
    if cohort == "picai":
        p2a_soft = dict(np.load(Path(str(PICAI_P2A_DET).format(seed=seed))))
    else:
        p2a_soft = dict(np.load(Path(str(P158_P2A_DET).format(seed=seed))))

    full_det = {c: np.squeeze(v) for c, v in p2a_soft.items() if c in labels_is_pos}
    full_labels = {c: labels_is_pos[c] for c in full_det.keys()}
    tau_full, sens_full, spec_full, status_full = find_matched_sens_threshold(
        full_det, full_labels, TARGET_SENS, MIN_VOXELS
    )

    rng = np.random.default_rng(seed)
    case_ids = list(full_det.keys())
    pos_ids = [c for c in case_ids if labels_is_pos[c]]
    neg_ids = [c for c in case_ids if not labels_is_pos[c]]
    rng.shuffle(pos_ids)
    rng.shuffle(neg_ids)
    n_fit_pos = max(1, len(pos_ids) // 3)
    n_fit_neg = max(1, len(neg_ids) // 3)
    eval_ids = pos_ids[n_fit_pos:] + neg_ids[n_fit_neg:]
    eval_det = {c: full_det[c] for c in eval_ids}
    eval_labels = {c: labels_is_pos[c] for c in eval_ids}
    tau_eval, sens_eval, spec_eval, status_eval = find_matched_sens_threshold(
        eval_det, eval_labels, TARGET_SENS, MIN_VOXELS
    )

    return {
        "method": "p2a_reference",
        "cohort": cohort,
        "seed": seed,
        "full_cohort": {
            "tau_matched_sens": tau_full,
            "achieved_casesens": sens_full,
            "achieved_casespec": spec_full,
            "status": status_full,
            "n": len(full_det),
        },
        "eval_subset_2_3": {
            "tau_matched_sens": tau_eval,
            "achieved_casesens": sens_eval,
            "achieved_casespec": spec_eval,
            "status": status_eval,
            "n": len(eval_det),
        },
    }


# ---------------------------------------------------------------------------
# Aggregation
# ---------------------------------------------------------------------------
def aggregate_method(per_seed_results: list[dict], key_path: list[str]) -> dict:
    """Return mean/sd over the value at `key_path` across seeds."""
    vals = []
    for r in per_seed_results:
        v = r
        try:
            for k in key_path:
                v = v[k]
        except (KeyError, TypeError):
            v = None
        if isinstance(v, (int, float)) and np.isfinite(v):
            vals.append(float(v))
    if not vals:
        return {"mean": None, "sd": None, "n": 0}
    arr = np.array(vals)
    return {
        "mean": float(arr.mean()),
        "sd": float(arr.std(ddof=1)) if len(arr) > 1 else 0.0,
        "n": len(arr),
        "per_seed": vals,
    }


