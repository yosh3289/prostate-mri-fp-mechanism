# ---------------------------------------------------------------------------
# Case-level metrics
# ---------------------------------------------------------------------------
def compute_case_sens_spec(
    detections: dict[str, np.ndarray],
    labels_is_pos: dict[str, bool],
    threshold: float,
    min_voxels: int = MIN_VOXELS,
) -> tuple[float, float, int, int]:
    tp = fp = tn = fn = 0
    for cid, det in detections.items():
        if cid not in labels_is_pos:
            continue
        binary = det > threshold
        if not binary.any():
            pred_pos = False
        else:
            labeled, n = ndimage.label(binary)
            if n == 0:
                pred_pos = False
            else:
                sizes = ndimage.sum(binary, labeled, range(1, n + 1))
                pred_pos = any(s >= min_voxels for s in sizes)
        is_pos = labels_is_pos[cid]
        if is_pos and pred_pos:
            tp += 1
        elif is_pos and not pred_pos:
            fn += 1
        elif not is_pos and pred_pos:
            fp += 1
        else:
            tn += 1
    sens = tp / (tp + fn) if (tp + fn) > 0 else float("nan")
    spec = tn / (tn + fp) if (tn + fp) > 0 else float("nan")
    return sens, spec, tp + fn, tn + fp


def find_matched_sens_threshold(
    detections: dict[str, np.ndarray],
    labels_is_pos: dict[str, bool],
    target_sens: float = TARGET_SENS,
    min_voxels: int = MIN_VOXELS,
) -> tuple[float, float, float, str]:
    """Find tau minimising |CaseSens - target_sens|; tie-break by higher spec.

    Returns (best_tau, achieved_sens, achieved_spec, status).
    status in {"ok", "saturated_min", "saturated_max", "no_valid"}.
    """
    thresholds = np.arange(0.005, 1.000, 0.005)
    best_tau, best_sens, best_spec = None, float("nan"), float("nan")
    best_diff = float("inf")
    all_sens_vals = []
    for t in thresholds:
        sens, spec, _, _ = compute_case_sens_spec(detections, labels_is_pos, float(t), min_voxels)
        if np.isnan(sens):
            continue
        all_sens_vals.append(sens)
        diff = abs(sens - target_sens)
        if diff < best_diff - 1e-9 or (
            abs(diff - best_diff) < 1e-9
            and (np.isnan(best_spec) or (spec > best_spec))
        ):
            best_tau, best_sens, best_spec = float(t), sens, spec
            best_diff = diff

    status = "ok"
    if all_sens_vals:
        # Degenerate: if all thresholds yield sens=1.0 (all positive) or sens=0.0
        max_sens, min_sens = max(all_sens_vals), min(all_sens_vals)
        if max_sens < target_sens - 0.01:
            status = "saturated_max"  # can never hit target: model too weak
        elif min_sens > target_sens + 0.05:
            status = "saturated_min"  # never falls below target: always-positive detector
    else:
        status = "no_valid"

    if best_tau is None:
        return float("nan"), float("nan"), float("nan"), "no_valid"
    return best_tau, best_sens, best_spec, status


