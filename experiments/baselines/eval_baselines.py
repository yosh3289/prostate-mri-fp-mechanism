#!/usr/bin/env python3
"""Competitive post-hoc baselines for P2a.

Three baselines, all operating on bare A2 outputs (no backbone modification):

  1. Temperature scaling (case level)
     - Fit scalar T on a held-out subset of PI-CAI fold-0 *train* cases
       by minimising case-level NLL (binary: is_positive? 1 per case,
       using the maximum class-1 softmax value over the volume as the
       predicted positive probability).
     - At inference: apply logits/T then softmax.
     - Case-level NLL chosen for consistency with our CaseSens/CaseSpec
       metrics (see design note below).

  2. Platt scaling (case level)
     - Logistic regression on (max_softmax_prob, case_is_positive) fit
       on a held-out subset.  Yields recalibrated case-level probabilities.

  3. Threshold sweep to matched-sens-optimal CaseSpec
     - For bare A2 softmax, find tau* that maximises CaseSpec subject to
       CaseSens >= 0.94 over the evaluation cohort.

Design notes
------------
* Case-level calibration is chosen (not voxel-level) because:
    (a) CaseSens / CaseSpec are the primary metrics and are case-level;
    (b) voxel-level calibration over millions of voxels is dominated by
        background (overwhelming class imbalance) and does not translate
        to case-level decisions;
    (c) the P2a refinement head is evaluated at the case level too.

* The "fit set" for temperature / Platt uses 100 cases randomly drawn from
  the PI-CAI fold-0 TRAIN split (not val, to avoid double-dipping).  Eval
  is always on the full 300-case val set (PI-CAI) or 158-case Prostate158.

Evaluation
----------
All methods are evaluated at matched sensitivity:
  - Target CaseSens = 0.94 (Task C convention, also our Paper 2 deployment point)
  - Find tau_match that brings CaseSens to >= 0.94 with minimal |sens - 0.94|;
    break ties toward higher CaseSpec.
  - Report CaseSpec at that tau for each method.

For temperature/Platt, "tau" is in the space of *recalibrated* probabilities;
for the threshold sweep and P2a (reference), it's in the original softmax space.

Inputs
------
  experiments/baselines/picai_val_bare_seed{S}.npz       (soft class-1 maps)
  experiments/baselines/picai_val_logits_bare_seed{S}.npz (fp16 class-0/1 logits)
  experiments/baselines/picai_val_p2a_seed{S}.npz        (P2a soft maps)
  experiments/baselines/picai_val_labels.npz             (GT)
  experiments/prostate158_p2a_bare/detections_seed{S}.npz
  experiments/prostate158_p2a/detections_seed{S}.npz
  Prostate158 labels (loaded from cache on the fly)

  Also needs a FIT set of bare logits on 100 PI-CAI *train* cases -- these are
  generated by run_picai_val_inference.py in a separate mode if needed.
  If the fit-set file is missing, temperature / Platt are fit on 100 cases
  sub-sampled from the eval val set (documented as "val-holdout") and eval
  is done on the remaining 200.  This is a fallback; preferred path is to
  use a held-out train subset.

Outputs
-------
  experiments/baselines/temperature_seed{S}_{cohort}.json
  experiments/baselines/platt_seed{S}_{cohort}.json
  experiments/baselines/threshold_sweep_seed{S}_{cohort}.json
  experiments/baselines/aggregate_comparison.json

Usage
-----
  conda run -n mar python experiments/baselines/eval_baselines.py
  conda run -n mar python experiments/baselines/eval_baselines.py --cohort picai
"""
