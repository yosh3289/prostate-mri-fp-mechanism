def load_picai_val_labels() -> dict[str, bool]:
    if not PICAI_LABELS.exists():
        raise FileNotFoundError(f"Labels sidecar missing: {PICAI_LABELS}")
    labels = dict(np.load(PICAI_LABELS))
    return {k: bool((v > 0).any()) for k, v in labels.items()}


def load_p158_labels() -> dict[str, bool]:
    out = {}
    for f in sorted(P158_CACHE.glob("*.pt")):
        d = torch.load(f, weights_only=False)
        pid = d["patient_id"]
        label = d["label"]
        arr = label.numpy() if hasattr(label, "numpy") else np.asarray(label)
        while arr.ndim > 3:
            arr = arr[0]
        out[pid] = bool((arr > 0.5).any())
    return out


# ---------------------------------------------------------------------------
# Temperature scaling (case level)
# ---------------------------------------------------------------------------
def case_max_prob(softmax_map: np.ndarray, min_voxels: int = MIN_VOXELS) -> float:
    """Return the max component-mean softmax prob (proxy for case-level prob).

    Actually: use the maximum voxel softmax (simpler, and robust).
    For case-level probability calibration, the operative signal is the
    detector's peak response, which is exactly what the CaseSens/CaseSpec
    threshold is applied to.
    """
    return float(np.squeeze(softmax_map).max())


def case_prob_from_logits(logits_01: np.ndarray, T: float) -> float:
    """Compute case-level max prob from [2, D, H, W] logits using temperature T."""
    # logits_01: [2, D, H, W]
    l0 = logits_01[0].astype(np.float32)
    l1 = logits_01[1].astype(np.float32)
    # temperature-scaled softmax for class 1
    # softmax_1 = exp(l1/T) / (exp(l0/T) + exp(l1/T)) = sigmoid((l1-l0)/T)
    d = (l1 - l0) / T
    # numerical stability:
    p = 1.0 / (1.0 + np.exp(-d))
    return float(p.max())


def case_softmax_map_from_logits(logits_01: np.ndarray, T: float = 1.0) -> np.ndarray:
    """Return a [D, H, W] class-1 softmax with temperature T."""
    l0 = logits_01[0].astype(np.float32)
    l1 = logits_01[1].astype(np.float32)
    d = (l1 - l0) / T
    return 1.0 / (1.0 + np.exp(-d))


def fit_temperature(case_probs_logits: np.ndarray, y_true: np.ndarray) -> float:
    """Fit scalar T by minimising case-level binary NLL on max-prob.

    case_probs_logits: N array of (l1-l0)_max (logit of max class-1 prob)
    y_true: N array of {0, 1}
    softmax prob = sigmoid(logit / T)
    """
    def nll(x):
        logT = float(np.asarray(x).ravel()[0])
        T = float(np.exp(logT))  # parameterise in log-space for positivity
        p = 1.0 / (1.0 + np.exp(-case_probs_logits / T))
        p = np.clip(p, 1e-7, 1 - 1e-7)
        return -np.mean(y_true * np.log(p) + (1 - y_true) * np.log(1 - p))

    # Bound T to [0.1, 100] (corresponds to logT in [-2.30, 4.61]).
    # The saturated A2 detector's NLL is minimised at T -> inf (everything
    # becomes uniform 0.5 and NLL = log(2)); we bound so that the post-hoc
    # T-scaling still applies a *meaningful* monotonic transform to the
    # softmax surface rather than degenerating to a constant.
    # Note: T-scaling is monotonic, so at matched-sens the CaseSpec is
    # identical to a threshold sweep; the bounded T here gives a consistent
    # recalibration surface for downstream deployment but does not alter
    # matched-sens CaseSpec in theory.  We use a bounded Nelder-Mead and
    # clip T in the returned value.
    res = optimize.minimize(
        nll, x0=np.array([0.0]), method="Nelder-Mead",
        options={"xatol": 1e-3, "fatol": 1e-6, "maxiter": 500},
    )
    T = float(np.exp(res.x[0]))
    T = max(0.1, min(T, 100.0))
    return T


def compute_case_logit(logits_01: np.ndarray) -> float:
    """Return max over voxels of (l1 - l0).  The case-level 'logit'."""
    l0 = logits_01[0].astype(np.float32)
    l1 = logits_01[1].astype(np.float32)
    return float((l1 - l0).max())


# ---------------------------------------------------------------------------
# Platt scaling (case level)
# ---------------------------------------------------------------------------
def fit_platt(max_probs: np.ndarray, y_true: np.ndarray) -> tuple[float, float]:
    """Logistic regression: p_calibrated = sigmoid(a * logit(p) + b).

    Fit on case-level (max_prob, y_true).
    """
    max_probs = np.clip(max_probs, 1e-7, 1 - 1e-7)
    logit_p = np.log(max_probs / (1 - max_probs))

    def nll(params):
        a, b = params
        z = a * logit_p + b
        # log_sigmoid for stability:
        # log-likelihood
        # p = sigmoid(z)
        # -ll = sum(log(1+exp(-y*(2y-1)*(...))) ...) use direct form
        p = 1.0 / (1.0 + np.exp(-z))
        p = np.clip(p, 1e-7, 1 - 1e-7)
        return -np.mean(y_true * np.log(p) + (1 - y_true) * np.log(1 - p))

    res = optimize.minimize(nll, x0=[1.0, 0.0], method="Nelder-Mead")
    a, b = float(res.x[0]), float(res.x[1])
    return a, b


def apply_platt(max_probs: np.ndarray, a: float, b: float) -> np.ndarray:
    mp = np.clip(max_probs, 1e-7, 1 - 1e-7)
    z = a * np.log(mp / (1 - mp)) + b
    return 1.0 / (1.0 + np.exp(-z))


# ---------------------------------------------------------------------------
# Baseline 1: Temperature scaling
# ---------------------------------------------------------------------------
def baseline_temperature(seed: int, cohort: str,
                         labels_is_pos: dict[str, bool]) -> dict:
    """Fit T on a 100-case subset of the eval cohort (held-out from evaluation),
    evaluate on the remaining cases at matched sens.

    Rationale: PI-CAI fold-0 train logits are not saved.  We could regenerate
    them, but the proper 'held-out for calibration' subset is perfectly
    achievable as a within-cohort 100-case holdout.  This gives a valid
    comparison: P2a, threshold-sweep, and temperature/Platt all evaluate on
    the same held-out set (200 PI-CAI val cases or 58 P158 cases).

    For the Prostate158 cohort we do NOT have raw logits; we fit T on the
    softmax-inverted logits (approximated by log(p/(1-p))) -- for single-class
    binary softmax this is equivalent to T scaling on (l1 - l0).
    """
    rng = np.random.default_rng(seed)

    if cohort == "picai":
        logits_path = Path(str(PICAI_BARE_LOGITS).format(seed=seed))
        if not logits_path.exists():
            raise FileNotFoundError(f"Bare logits missing: {logits_path}")
        bare_logits = dict(np.load(logits_path))  # [2, D, H, W] fp16 per case
        bare_soft_path = Path(str(PICAI_BARE_DET).format(seed=seed))
        bare_soft = dict(np.load(bare_soft_path))
    else:  # prostate158
        bare_soft_path = Path(str(P158_BARE_DET).format(seed=seed))
        bare_soft = dict(np.load(bare_soft_path))
        bare_logits = None  # fall back to softmax-derived logits

    # Determine held-out split: 1/3 fit, 2/3 eval (approx 100/200 or 52/106)
    case_ids = [c for c in bare_soft.keys() if c in labels_is_pos]
    if len(case_ids) == 0:
        raise RuntimeError(f"No overlap between detections and labels for seed={seed} cohort={cohort}")

    # Stratified split by positivity to avoid all-neg fit sets
    pos_ids = [c for c in case_ids if labels_is_pos[c]]
    neg_ids = [c for c in case_ids if not labels_is_pos[c]]
    rng.shuffle(pos_ids)
    rng.shuffle(neg_ids)
    n_fit_pos = max(1, len(pos_ids) // 3)
    n_fit_neg = max(1, len(neg_ids) // 3)
    fit_ids = pos_ids[:n_fit_pos] + neg_ids[:n_fit_neg]
    eval_ids = pos_ids[n_fit_pos:] + neg_ids[n_fit_neg:]

    # Compute case-level max logits on fit set
    fit_logits = []
    fit_y = []
    for cid in fit_ids:
        if bare_logits is not None and cid in bare_logits:
            lg = bare_logits[cid]  # [2, D, H, W] fp16
            case_lgt = compute_case_logit(lg)
        else:
            mp = float(np.squeeze(bare_soft[cid]).max())
            mp = max(min(mp, 1 - 1e-7), 1e-7)
            case_lgt = float(np.log(mp / (1 - mp)))
        fit_logits.append(case_lgt)
        fit_y.append(int(labels_is_pos[cid]))

    fit_logits = np.array(fit_logits, dtype=np.float64)
    fit_y = np.array(fit_y, dtype=np.float64)

    T = fit_temperature(fit_logits, fit_y)
    log.info("[%s seed=%d] fit temperature T=%.4f on %d cases (%d pos, %d neg)",
             cohort, seed, T, len(fit_ids), n_fit_pos, n_fit_neg)

    # Build recalibrated softmax maps over eval set
    recalibrated = {}
    for cid in eval_ids:
        if bare_logits is not None and cid in bare_logits:
            lg = bare_logits[cid]
            recal = case_softmax_map_from_logits(lg, T=T).astype(np.float32)
        else:
            # Softmax-only path: p_recal = sigmoid( (l1-l0)/T ) where
            # (l1-l0) = log(p/(1-p)).  Equivalent to p^(1/T) / (p^(1/T) + (1-p)^(1/T))
            p = np.squeeze(bare_soft[cid]).astype(np.float32)
            p = np.clip(p, 1e-7, 1 - 1e-7)
            logit = np.log(p / (1 - p))
            recal = 1.0 / (1.0 + np.exp(-logit / T))
            recal = recal.astype(np.float32)
        recalibrated[cid] = recal

    # Match sens on eval set
    eval_labels = {cid: labels_is_pos[cid] for cid in eval_ids}
    tau, sens, spec, status = find_matched_sens_threshold(recalibrated, eval_labels, TARGET_SENS, MIN_VOXELS)

    return {
        "method": "temperature_scaling",
        "cohort": cohort,
        "seed": seed,
        "temperature_T": T,
        "n_fit": len(fit_ids),
        "n_eval": len(eval_ids),
        "tau_matched_sens": tau,
        "achieved_casesens": sens,
        "achieved_casespec": spec,
        "status": status,
    }


# ---------------------------------------------------------------------------
# Baseline 2: Platt scaling
# ---------------------------------------------------------------------------
def baseline_platt(seed: int, cohort: str, labels_is_pos: dict[str, bool]) -> dict:
    rng = np.random.default_rng(seed + 1)
    if cohort == "picai":
        bare_soft = dict(np.load(Path(str(PICAI_BARE_DET).format(seed=seed))))
    else:
        bare_soft = dict(np.load(Path(str(P158_BARE_DET).format(seed=seed))))

    case_ids = [c for c in bare_soft.keys() if c in labels_is_pos]
    pos_ids = [c for c in case_ids if labels_is_pos[c]]
    neg_ids = [c for c in case_ids if not labels_is_pos[c]]
    rng.shuffle(pos_ids)
    rng.shuffle(neg_ids)
    n_fit_pos = max(1, len(pos_ids) // 3)
    n_fit_neg = max(1, len(neg_ids) // 3)
    fit_ids = pos_ids[:n_fit_pos] + neg_ids[:n_fit_neg]
    eval_ids = pos_ids[n_fit_pos:] + neg_ids[n_fit_neg:]

    fit_max = np.array([float(np.squeeze(bare_soft[c]).max()) for c in fit_ids])
    fit_y = np.array([int(labels_is_pos[c]) for c in fit_ids], dtype=np.float64)

    a, b = fit_platt(fit_max, fit_y)
    log.info("[%s seed=%d] fit Platt a=%.4f b=%.4f on %d cases",
             cohort, seed, a, b, len(fit_ids))

    # Apply Platt scaling per-voxel: we use the per-voxel softmax to build
    # a recalibrated map so that thresholding at a calibrated prob still
    # works voxel-wise.
    recalibrated = {}
    for cid in eval_ids:
        p = np.squeeze(bare_soft[cid]).astype(np.float32)
        p = np.clip(p, 1e-7, 1 - 1e-7)
        logit = np.log(p / (1 - p))
        z = a * logit + b
        recal = 1.0 / (1.0 + np.exp(-z))
        recalibrated[cid] = recal.astype(np.float32)

    eval_labels = {cid: labels_is_pos[cid] for cid in eval_ids}
    tau, sens, spec, status = find_matched_sens_threshold(recalibrated, eval_labels, TARGET_SENS, MIN_VOXELS)

    return {
        "method": "platt_scaling",
        "cohort": cohort,
        "seed": seed,
        "platt_a": a,
        "platt_b": b,
        "n_fit": len(fit_ids),
        "n_eval": len(eval_ids),
        "tau_matched_sens": tau,
        "achieved_casesens": sens,
        "achieved_casespec": spec,
        "status": status,
    }


# ---------------------------------------------------------------------------
# Baseline 3: Threshold sweep to matched sensitivity-optimal
# ---------------------------------------------------------------------------
def baseline_threshold_sweep(seed: int, cohort: str, labels_is_pos: dict[str, bool]) -> dict:
    """Raw bare A2: find tau matching CaseSens>=0.94 with best spec.

    NOTE: This is evaluated on the FULL cohort (all 300 PI-CAI val or 158 P158
    cases) because threshold sweeping does not require a fit/eval split --
    it's a deterministic function of the detection output.  However, for fair
    comparison with temperature/Platt (which are fit on 1/3 and eval on 2/3),
    we also report results on the same 2/3 eval subset.
    """
    if cohort == "picai":
        bare_soft = dict(np.load(Path(str(PICAI_BARE_DET).format(seed=seed))))
    else:
        bare_soft = dict(np.load(Path(str(P158_BARE_DET).format(seed=seed))))

    # Full-cohort eval (primary report)
    full_det = {c: np.squeeze(v) for c, v in bare_soft.items() if c in labels_is_pos}
    full_labels = {c: labels_is_pos[c] for c in full_det.keys()}
    tau_full, sens_full, spec_full, status_full = find_matched_sens_threshold(
        full_det, full_labels, TARGET_SENS, MIN_VOXELS
    )

    # Same 2/3 split as temperature/Platt
    rng = np.random.default_rng(seed)
    case_ids = list(full_det.keys())
    pos_ids = [c for c in case_ids if labels_is_pos[c]]
    neg_ids = [c for c in case_ids if not labels_is_pos[c]]
    rng.shuffle(pos_ids)
    rng.shuffle(neg_ids)
    n_fit_pos = max(1, len(pos_ids) // 3)
    n_fit_neg = max(1, len(neg_ids) // 3)
    eval_ids = pos_ids[n_fit_pos:] + neg_ids[n_fit_neg:]
    eval_det = {c: full_det[c] for c in eval_ids}
    eval_labels = {c: labels_is_pos[c] for c in eval_ids}
    tau_eval, sens_eval, spec_eval, status_eval = find_matched_sens_threshold(
        eval_det, eval_labels, TARGET_SENS, MIN_VOXELS
    )

    return {
        "method": "threshold_sweep",
        "cohort": cohort,
        "seed": seed,
        "full_cohort": {
            "tau_matched_sens": tau_full,
            "achieved_casesens": sens_full,
            "achieved_casespec": spec_full,
            "status": status_full,
            "n": len(full_det),
        },
        "eval_subset_2_3": {
            "tau_matched_sens": tau_eval,
            "achieved_casesens": sens_eval,
            "achieved_casespec": spec_eval,
            "status": status_eval,
            "n": len(eval_det),
        },
    }


